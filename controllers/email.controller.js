const { serverErrorHandler } = require('../lib/handlerReuse');
const { verifyRequestToken } = require('../lib/responseReuse');
const {
  archiveEmailService,
  createEmailService,
  deleteEmailService,
  getAllEmailService,
  getSpecificEmailService,
  readEmailService,
} = require('../services/email.service');

async function createEmailController(req, res) {
  const token = req.headers.authorization?.split(' ')[1];
  const payloads = verifyRequestToken(token);

  if (payloads) {
    return res.status(payloads.code).json(payloads);
  }

  try {
    const payloads = await createEmailService({
      requestToken: token,
      body: req.body,
    });

    return res.status(payloads.code).json(payloads);
  } catch (error) {
    console.error(error);
  
    return serverErrorHandler(res);
  }
}

